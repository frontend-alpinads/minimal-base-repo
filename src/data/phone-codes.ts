// Get phone prefix for a country code
export function getPhonePrefix(countryCode: string): string {
  return COUNTRY_TO_PHONE_PREFIX[countryCode] || "";
}

// Get country code from phone prefix
export function getCountryFromPrefix(prefix: string): string | undefined {
  return Object.keys(COUNTRY_TO_PHONE_PREFIX).find(
    (key) => COUNTRY_TO_PHONE_PREFIX[key] === prefix,
  );
}
